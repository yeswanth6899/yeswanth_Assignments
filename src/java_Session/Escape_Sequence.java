package java_Session;

public class Escape_Sequence {
	
	public static void main(String[] args) {
		
		System.out.println("Example of newline:\nThis is the next line.");
        System.out.println("Example of tab:\tTab space used here.");
        System.out.println("Example of backslash: \\");
        System.out.println("Example of double quote: \"Java\"");
        System.out.println("Example of single quote: It\'s easy.");

        System.out.println("\n\t---- RECEIPT ----");
        System.out.println("Item\t\t\tPrice");
        System.out.println("Coffee\t\t\tRs.50");
        System.out.println("Sandwich\t\tRs.120");
	}

}
